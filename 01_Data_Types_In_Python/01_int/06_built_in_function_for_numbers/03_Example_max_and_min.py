# Finding highest and lowest score
scores = [85, 92, 78, 88, 95]
highest = max(scores)
lowest = min(scores)
print(f"Highest: {highest}, Lowest: {lowest}") # Output: Highest: 95, Lowest: 78

# Find temperature range
temperatures = [15, 22, 18, 25, 20]
max_temp = max(temperatures)
min_temp = min(temperatures)
print(f"Temperatures range: {min_temp} degree celsius to {max_temp} degree celsius")
# Output: Temperature range: 15 degree celcius to 25 degree celcius